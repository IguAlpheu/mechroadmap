// ── Level calculation based on timer hours + steps ──
export function calculateLevel(
  goalId: string,
  timerHours: number,
  completedSteps: number,
  totalSteps: number,
  sessionRatings: number[] // array of ratings: 1=bad, 2=ok, 3=good
): ChallengeLevel {
  // Step completion ratio (0-1)
  const stepRatio = totalSteps > 0 ? completedSteps / totalSteps : 0;

  // Quality score from session ratings (0-1)
  const avgRating = sessionRatings.length > 0
    ? sessionRatings.reduce((a, b) => a + b, 0) / sessionRatings.length / 3
    : 0.5;

  // Combined score weighted: 50% hours, 30% steps, 20% quality
  const hourTargets: Record<string, number> = {
    coding: 400, japanese: 350, discipline: 200,
    calculus: 300, mechatronics: 350, flstudio: 250, webdev: 300,
  };
  const target = hourTargets[goalId] || 300;
  const hourRatio = Math.min(timerHours / target, 1);

  const score = hourRatio * 0.5 + stepRatio * 0.3 + avgRating * 0.2;

  if (score >= 0.7) return 'advanced';
  if (score >= 0.3) return 'intermediate';
  return 'beginner';
}

// ── Get today's challenge for a goal ──
export function getDailyChallenge(goalId: string, level: ChallengeLevel): string {
  const bank = challengeBank[goalId]?.[level] || challengeBank[goalId]?.beginner || [];
  if (bank.length === 0) return "Complete one study session today";

  // Use date as seed so it changes daily but is consistent within a day
  const today = new Date();
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();
  // XOR with goalId hash for uniqueness per goal
  const goalHash = goalId.split('').reduce((a, c) => a + c.charCodeAt(0), 0);
  const index = (seed + goalHash) % bank.length;

  return bank[index];
}
